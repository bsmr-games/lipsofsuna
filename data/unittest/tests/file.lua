Unittest:add(1, "system", "file", function()
	local File = require("system/file")
	-- Directory scanning.
	local r1 = File:scan_directory("/")
	assert(#r1 == 4)
	assert(r1[1] == "aer1-walk.lani")
	assert(r1[2] == "config.ld")
	assert(r1[3] == "main.lua")
	assert(r1[4] == "tests")
	local r2 = File:scan_directory("..")
	assert(r2 == nil)
	local r3 = File:scan_directory("")
	assert(#r3 == 4)
	assert(r3[3] == "main.lua")
	local r4 = File:scan_directory(".")
	assert(#r4 == 4)
	assert(r4[3] == "main.lua")
	local r5 = File:scan_directory("./")
	assert(#r5 == 4)
	assert(r5[3] == "main.lua")
end)
