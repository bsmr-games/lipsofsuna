Unittest:add(1, "system", "animation", function()
	local Animation = require("system/animation")
	local a = Animation:load("aer1-walk")
	assert(a)
	assert(a:get_duration() > 0)
	assert(a:get_frames() > 0)
	local a = Animation:load("adfdsf")
	assert(a == nil)
	local a = Animation("fadfdsf")
	assert(a ~= nil)
	assert(a:get_duration() == 1)
	assert(a:get_frames() == 0)
end)
