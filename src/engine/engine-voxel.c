/**
 * \brief Initializes a voxel of the given shape and type.
 *
 * If the shape denotes a degenerate object or if the type is zero,
 * an empty voxel is created. Otherwise, a voxel of the provided
 * shape and type is created.
 *
 * \param shape Shape mask.
 * \param type Terrain type.
 * \return Voxel.
 */
liengTile
lieng_voxel_init (int shape,
                  int type)
{
	type &= 0xFF;
	shape &= 0xFF;
	if (!type || !shape || (shape != 0xFF && !voxel_triangle_lists[shape].count))
		return 0;
	return type | (shape << 8);
}

/**
 * \brief Triangulates a voxel.
 *
 * The neighbor tiles must be provided in the following format:
 *
 * \li Voxel adjancent to the negative X face.
 * \li Voxel adjancent to the positive X face.
 * \li Voxel adjancent to the negative Y face.
 * \li Voxel adjancent to the positive Y face.
 * \li Voxel adjancent to the negative Z face.
 * \li Voxel adjancent to the positive Z face.
 *
 * \param self Voxel.
 * \param neighbors Array of adjanced voxels.
 * \param result Return location with room for at least 36(?) vertices.
 * \return Number of vertices written.
 */
int
lieng_voxel_triangulate (liengTile    self,
                         liengTile*   neighbors,
                         limatVector* result)
{
	int c;
	int i;
	int j;
	int n;

	/* Triangulate cross section. */
	i = (self >> 8);
	for (c = 0 ; c < 3 * voxel_triangle_lists[i].count ; c++)
		result[c] = voxel_triangle_lists[i].array[c];

	/* Triangulate unit cube. */
	for (i = 0 ; i < 6 ; i++)
	{
		for (j = n = 0 ; j < 4 && n < 2 ; j++)
		{
			if ((voxel_unitcube_triangle_masks[i][0][j] & self) ==
			     voxel_unitcube_triangle_masks[i][0][j] &&
			    (voxel_unitcube_triangle_masks[i][1][j] & neighbors[i]) !=
			     voxel_unitcube_triangle_masks[i][1][j])
			{
				result[c++] = voxel_unitcube_triangle_vertices[4 * i + j][0];
				result[c++] = voxel_unitcube_triangle_vertices[4 * i + j][1];
				result[c++] = voxel_unitcube_triangle_vertices[4 * i + j][2];
				n++;
			}
		}
	}

	return c;
}

/**
 * \brief Converts degenerate cases to valid voxel types.
 *
 * If the voxel shape is degenerate, returns an empty voxel. Otherwise,
 * returns the original voxel.
 *
 * \param self Voxel.
 * \return Voxel.
 */
liengTile
lieng_voxel_validate (liengTile self)
{
	int shape = lieng_voxel_get_shape (self);
	int type = lieng_voxel_get_type (self);

	return lieng_voxel_init (shape, type);
}

/**
 * \brief Gets the shape mask of the voxel.
 *
 * \param self Voxel.
 * \return Shape mask.
 */
int
lieng_voxel_get_shape (liengTile self)
{
	return self >> 8;
}

/**
 * \brief Gets the terrain type of the voxel.
 *
 * \param self Voxel.
 * \return Terrain type.
 */
int
lieng_voxel_get_type (liengTile self)
{
	return self & 0xFF;
}

