/**
 * \brief Initializes a voxel of the given shape and type.
 *
 * If the shape denotes a degenerate object or if the type is zero,
 * an empty voxel is created. Otherwise, a voxel of the provided
 * shape and type is created.
 *
 * \param shape Shape mask.
 * \param type Terrain type.
 * \return Voxel.
 */
liengTile
lieng_voxel_init (int shape,
                  int type)
{
	type &= 0xFF;
	shape &= 0xFF;
	if (!type || !shape || (shape != 0xFF && !voxel_crosssection_triangles[shape].count))
		return 0;
	return type | (shape << 8);
}

/**
 * \brief Triangulates a voxel.
 *
 * The neighbor tiles must be provided in the following format:
 *
 * \li Voxel adjancent to the negative X face.
 * \li Voxel adjancent to the positive X face.
 * \li Voxel adjancent to the negative Y face.
 * \li Voxel adjancent to the positive Y face.
 * \li Voxel adjancent to the negative Z face.
 * \li Voxel adjancent to the positive Z face.
 *
 * \param self Voxel.
 * \param neighbors Array of adjanced voxels.
 * \param result Return location with room for at least 36(?) vertices.
 * \return Number of vertices written.
 */
int
lieng_voxel_triangulate (liengTile    self,
                         liengTile*   neighbors,
                         limdlVertex* result)
{
	int c;
	int i;
	int j;
	int n;
	limatVector normal = { 0.0f, 0.0f, 0.0f };

	/* Triangulate cross section. */
	i = (self >> 8);
	for (c = 0 ; c < 3 * voxel_crosssection_triangles[i].count ; c += 3)
	{
		normal = limat_vector_normalize (limat_vector_cross (
			limat_vector_subtract (
				voxel_crosssection_triangles[i].array[c + 0],
				voxel_crosssection_triangles[i].array[c + 1]),
			limat_vector_subtract (
				voxel_crosssection_triangles[i].array[c + 1],
				voxel_crosssection_triangles[i].array[c + 2])));
		limdl_vertex_init (result + c + 0,
			voxel_crosssection_triangles[i].array + c + 0, &normal,
			voxel_crosssection_texcoords[i][c + 0][0],
			voxel_crosssection_texcoords[i][c + 0][1]);
		limdl_vertex_init (result + c + 1,
			voxel_crosssection_triangles[i].array + c + 1, &normal,
			voxel_crosssection_texcoords[i][c + 1][0],
			voxel_crosssection_texcoords[i][c + 1][1]);
		limdl_vertex_init (result + c + 2,
			voxel_crosssection_triangles[i].array + c + 2, &normal,
			voxel_crosssection_texcoords[i][c + 2][0],
			voxel_crosssection_texcoords[i][c + 2][1]);
	}

	/* Triangulate unit cube. */
	for (i = 0 ; i < 6 ; i++)
	{
		for (j = n = 0 ; j < 4 && n < 2 ; j++)
		{
			if ((voxel_unitcube_triangle_masks[i][0][j] & self) ==
			     voxel_unitcube_triangle_masks[i][0][j] &&
			    (voxel_unitcube_triangle_masks[i][1][j] & neighbors[i]) !=
			     voxel_unitcube_triangle_masks[i][1][j])
			{
				normal = limat_vector_normalize (limat_vector_cross (
					limat_vector_subtract (
						voxel_unitcube_triangle_vertices[4 * i + j][0],
						voxel_unitcube_triangle_vertices[4 * i + j][1]),
					limat_vector_subtract (
						voxel_unitcube_triangle_vertices[4 * i + j][1],
						voxel_unitcube_triangle_vertices[4 * i + j][2])));
				limdl_vertex_init (result + c++,
					voxel_unitcube_triangle_vertices[4 * i + j] + 0, &normal,
					voxel_unitcube_triangle_texcoords[4 * i + j][0][0],
					voxel_unitcube_triangle_texcoords[4 * i + j][0][1]);
				limdl_vertex_init (result + c++,
					voxel_unitcube_triangle_vertices[4 * i + j] + 1, &normal,
					voxel_unitcube_triangle_texcoords[4 * i + j][1][0],
					voxel_unitcube_triangle_texcoords[4 * i + j][1][1]);
				limdl_vertex_init (result + c++,
					voxel_unitcube_triangle_vertices[4 * i + j] + 2, &normal,
					voxel_unitcube_triangle_texcoords[4 * i + j][2][0],
					voxel_unitcube_triangle_texcoords[4 * i + j][2][1]);
				n++;
			}
		}
	}

	/* FIXME: It would be possible to calculate the smooth normals
	 * here by triangulating the neighbor voxels and calculating
	 * the average normal of each vertex.
	 */

	return c;
}

/**
 * \brief Converts degenerate cases to valid voxel types.
 *
 * If the voxel shape is degenerate, returns an empty voxel. Otherwise,
 * returns the original voxel.
 *
 * \param self Voxel.
 * \return Voxel.
 */
liengTile
lieng_voxel_validate (liengTile self)
{
	int shape = lieng_voxel_get_shape (self);
	int type = lieng_voxel_get_type (self);

	return lieng_voxel_init (shape, type);
}

/**
 * \brief Gets the shape mask of the voxel.
 *
 * \param self Voxel.
 * \return Shape mask.
 */
int
lieng_voxel_get_shape (liengTile self)
{
	return self >> 8;
}

/**
 * \brief Gets the terrain type of the voxel.
 *
 * \param self Voxel.
 * \return Terrain type.
 */
int
lieng_voxel_get_type (liengTile self)
{
	return self & 0xFF;
}

